#include "Game/Stack/BaseFactory.hpp"

class IState;
//----------------------------------------------------------------------------------------------------
void BaseFactory::Register(String const&  name,
                           Creator const& creator)
{
    creators[name] = creator;
}

//----------------------------------------------------------------------------------------------------
IState* BaseFactory::Create(String const& name) const
{
    auto it = creators.find(name);
    if (it != creators.end())
    {
        return it->second();
    }
    return nullptr;
}
